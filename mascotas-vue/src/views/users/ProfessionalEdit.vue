<template>
  <div>
    <TitleBar title="Editar tu perfil"></TitleBar>
    <Loader v-if="loading"></Loader>
    <div v-else class="professional-form-container">
      <Form
        :user_types="user_types"
        :professional="professional"
      ></Form>
    </div>
    
    </div>
</template>

<script>

import Form from "@/components/users/professionals/Form";
import userService from "@/services/users"
import store from "@/store";
import TitleBar from "@/components/general/layouts/TitleBar";
import Loader from "@/components/general/notifications/Loader";


export default {
  name: "ProfessionalEdit",
  components: {
    Form,
    Loader,
    TitleBar,
 },
  data: () => ({
    loading: true,
    user_types: [],
    professional: null,
    store,
  }),
  mounted() {
    userService.getProfessionalUserTypes()
        .then(res => {
          this.user_types = res.data.user_types;

          userService.getUserById(this.store.user.id)
            .then(res => {
                if (res.data.user) {
                    this.professional = res.data.user;
                } else {
                    this.store.setStatus({
                        msg: 'Hubo un error mientras cargamos tus datos. Por favor intentalo m√°s tarde.',
                        type: 'error',
                    });
                    this.$router.go(-1);
                }
                this.loading = false;
            })
            
        })
  }
}
</script>

